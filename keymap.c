static void set_frame(const uint16_t* frame_delays, const int number_of_frame) {
    if (timer_elapsed(timer) > FRAME_DURATION * frame_delays[current_frame]) {
        timer = timer_read();
        current_frame = (current_frame + 1) % number_of_frame;
    }
}

static void update_frame(const char** frame_array, const uint16_t* frame_sizes, const uint16_t* frame_delays, const int number_of_frame) {
    if (get_current_wpm() > 000) {
        idle_timer = timer_read();
        is_idle = false;
        oled_on();
        set_frame(frame_delays, number_of_frame);
        oled_write_raw_P(frame_array[current_frame], frame_sizes[current_frame]);
    } else if (timer_elapsed(idle_timer) > 60000) {
        is_idle = true;
        oled_off();
    } else if (!is_idle) {
        oled_on();
        set_frame(frame_delays, number_of_frame);
        oled_write_raw_P(frame_array[current_frame], frame_sizes[current_frame]);
    }
}

