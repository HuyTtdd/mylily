oled_rotation_t oled_init_user(oled_rotation_t rotation) {
  if (!is_keyboard_master())
    return OLED_ROTATION_180;  // flips the display 180 degrees on 'not master' side
  if (is_keyboard_master())
    return OLED_ROTATION_270;  // flips the display 270 degrees on master side
  return rotation;
}

